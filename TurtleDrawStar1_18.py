# Draw 5 point star using turtle

import turtle

turtle.penup()
turtle.goto(-50,-50)
turtle.pendown()
turtle.left(72)
turtle.color("blue")
turtle.forward(300)
turtle.right(144)
turtle.color("orange")
turtle.forward(300)
turtle.right(144)
turtle.color("green")
turtle.forward(300)
turtle.right(144)
turtle.color("purple")
turtle.forward(300)
turtle.right(144)
turtle.color("red")
turtle.forward(300)
turtle.exitonclick()

